class Calculator {
  static int add(int a, int b) {
    return a + b;
  }

  static int substract(int a, int b) {
    return a - b;
  }

  static int multiply(int a, int b) {
    return a * b;
  }

  static double divide(int a, int b) {
    return a / b;
  }

  // static int modulus(int a, int b);
}
